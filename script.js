// HERO TITLE LETTER ANIMATION
// ===========================
(function initHeroLetterAnimation() {
  try {
    const heroTitle = document.querySelector('.hero h1');
    if (!heroTitle) return;

    // Wait for page to be ready
    setTimeout(() => {
      let letterIndex = 0;

      // Recursively process text nodes, preserving all element structure
      function wrapLetters(node) {
        if (node.nodeType === Node.TEXT_NODE) {
          const text = node.textContent;
          if (!text.trim()) return; // skip whitespace-only nodes
          const frag = document.createDocumentFragment();
          for (let i = 0; i < text.length; i++) {
            if (text[i] === ' ') {
              frag.appendChild(document.createTextNode(' '));
            } else {
              const span = document.createElement('span');
              span.className = 'hero-letter';
              span.style.animationDelay = (0.5 + letterIndex * 0.04) + 's';
              span.textContent = text[i];
              frag.appendChild(span);
              letterIndex++;
            }
          }
          node.parentNode.replaceChild(frag, node);
        } else if (node.nodeType === Node.ELEMENT_NODE) {
          // Process child nodes in reverse to avoid index shifting
          const children = Array.from(node.childNodes);
          children.forEach(child => wrapLetters(child));
        }
      }

      wrapLetters(heroTitle);
    }, 100);
  } catch(e) { console.error('HeroLetterAnimation error:', e); }
})();


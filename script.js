(function initSectionTransition() {
  try {
  // Section accent colors (mapped to major sections)
  const sectionColors = {
    'greek':    'rgba(212, 168, 67, 0.06)',   // Golden
    'fall':     'rgba(180, 50, 30, 0.06)',     // Blood red
    'norse':    'rgba(80, 160, 210, 0.06)',    // Ice blue
    'prophecy': 'rgba(160, 80, 200, 0.06)'    // Mystical purple
  };

  // Observe sections with matching IDs
  const sections = document.querySelectorAll('section[id]');
  if (!sections.length) return;

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const id = entry.target.id.toLowerCase();

        // Find matching section color
        for (const [key, color] of Object.entries(sectionColors)) {
          if (id.includes(key)) {
            window.__cursorGlowColor = color;
            return;
          }
        }

        // Default golden if no match
        window.__cursorGlowColor = sectionColors['greek'];
      }
    });
  }, {
    rootMargin: '-40% 0px -40% 0px',
    threshold: 0
  });

  sections.forEach(s => observer.observe(s));
  } catch(e) {
    handleScriptError(e, "initSectionTransition");
  }
})();



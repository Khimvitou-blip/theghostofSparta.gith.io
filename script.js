// 11. 3D TILT EFFECT (Character + God-Kill Cards) with Light Reflection
// ===========================
(function initTiltCards() {
  try {
  const cards = document.querySelectorAll('.character-card, .god-kill-card');
  if (!cards.length) return;

  const MAX_TILT = 12;
  const LERP_FACTOR = 0.1;

  cards.forEach(card => {
    let currentRotateX = 0;
    let currentRotateY = 0;
    let targetRotateX = 0;
    let targetRotateY = 0;
    let lightX = 50;
    let lightY = 50;
    let animating = false;
    let isHovering = false;

    // Create light reflection overlay
    const lightOverlay = document.createElement('div');
    Object.assign(lightOverlay.style, {
      position: 'absolute',
      top: '0',
      left: '0',
      width: '100%',
      height: '100%',
      borderRadius: 'inherit',
      pointerEvents: 'none',
      zIndex: '2',
      opacity: '0',
      transition: 'opacity 0.3s ease',
      background: 'radial-gradient(circle at 50% 50%, rgba(255,255,255,0.12) 0%, transparent 60%)'
    });
    card.style.position = card.style.position || 'relative';
    card.style.overflow = card.style.overflow || 'hidden';
    card.appendChild(lightOverlay);

    function animateTilt() {
      if (!animating) return;

      // Lerp toward target
      currentRotateX += (targetRotateX - currentRotateX) * LERP_FACTOR;
      currentRotateY += (targetRotateY - currentRotateY) * LERP_FACTOR;

      card.style.transform =
        `perspective(800px) rotateX(${currentRotateX}deg) rotateY(${currentRotateY}deg) translateY(-8px)`;

      // Update light position
      lightOverlay.style.background =
        `radial-gradient(circle at ${lightX}% ${lightY}%, rgba(255,255,255,0.15) 0%, transparent 60%)`;

      // Continue if still moving
      const deltaX = Math.abs(targetRotateX - currentRotateX);
      const deltaY = Math.abs(targetRotateY - currentRotateY);

      if (deltaX > 0.01 || deltaY > 0.01 || isHovering) {
        requestAnimationFrame(animateTilt);
      } else {
        animating = false;
        if (!isHovering) {
          card.style.transform = '';
        }
      }
    }

    card.addEventListener('mousemove', (e) => {
      isHovering = true;
      const rect = card.getBoundingClientRect();
      const x = e.clientX - rect.left;
      const y = e.clientY - rect.top;
      const centerX = rect.width / 2;
      const centerY = rect.height / 2;

      targetRotateX = ((y - centerY) / centerY) * -MAX_TILT;
      targetRotateY = ((x - centerX) / centerX) * MAX_TILT;

      // Light follows cursor
      lightX = (x / rect.width) * 100;
      lightY = (y / rect.height) * 100;
      lightOverlay.style.opacity = '1';

      if (!animating) {
        animating = true;
        requestAnimationFrame(animateTilt);
      }
    });

    card.addEventListener('mouseleave', () => {
      isHovering = false;
      targetRotateX = 0;
      targetRotateY = 0;
      lightOverlay.style.opacity = '0';

      // Spring easing back to flat
      card.style.transition = 'transform 0.6s cubic-bezier(0.34, 1.56, 0.64, 1)';
      card.style.transform = '';

      setTimeout(() => {
        card.style.transition = '';
        animating = false;
      }, 600);
    });
  });
  } catch(e) {
    handleScriptError(e, "initTiltCards");
  }
})();



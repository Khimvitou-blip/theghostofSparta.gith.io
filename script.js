// 20. COUNTER ANIMATION (Smooth Cubic Bezier Easing)
// ===========================
(function initChapterCounters() {
  try {
  const numbers = document.querySelectorAll('.chapter-number');

  numbers.forEach(num => {
    const target = parseInt(num.textContent, 10);
    num.dataset.target = target;
    num.textContent = '00';
  });

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const el = entry.target;
        const target = parseInt(el.dataset.target, 10);
        animateCount(el, 0, target, 800);
        observer.unobserve(el);
      }
    });
  }, { threshold: 0.5 });

  numbers.forEach(n => observer.observe(n));

  /**
   * Cubic bezier easing approximation for counter
   * Mimics cubic-bezier(0.25, 0.1, 0.25, 1.0) — standard ease
   */
  function cubicBezierEase(t) {
    // Attempt a cubic bezier feel: ease-in-out with overshoot dampening
    return t < 0.5
      ? 4 * t * t * t
      : 1 - Math.pow(-2 * t + 2, 3) / 2;
  }

  function animateCount(el, start, end, duration) {
    const startTime = performance.now();
    function tick(now) {
      const elapsed = now - startTime;
      const progress = Math.min(elapsed / duration, 1);
      const eased = cubicBezierEase(progress);
      const current = Math.round(start + (end - start) * eased);
      el.textContent = String(current).padStart(2, '0');
      if (progress < 1) requestAnimationFrame(tick);
    }
    requestAnimationFrame(tick);
  }
  } catch(e) {
    handleScriptError(e, "initChapterCounters");
  }
})();


